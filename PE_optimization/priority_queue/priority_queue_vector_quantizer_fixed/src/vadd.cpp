template<const int query_num, const int iter_num_per_query>
void read_input(const float* data_source, hls::stream<dist_cell_ID_t> &s_input) {

    for (int query_id = 0; query_id < query_num; query_id++) {

        for (int iter = 0; iter < iter_num_per_query; iter++) {
#pragma HLS pipeline II=1
            dist_cell_ID_t reg;
            reg.cell_ID = query_id * iter_num_per_query + iter;
            reg.dist = data_source[query_id * iter_num_per_query + iter];
            s_input.write(reg);
        }
    }
}

template<const int query_num, const int priority_queue_len>
void write_result(hls::stream<dist_cell_ID_t> &s_output, ap_uint<64>* output) {

    // only write the last value
    dist_cell_ID_t reg;

    for (int query_id = 0; query_id < query_num; query_id++) {

        for (int iter = 0; iter < priority_queue_len; iter++) {
#pragma HLS pipeline II=1
            dist_cell_ID_t reg = s_output.read();
        }
    }

    int cell_ID = reg.cell_ID;
    float dist = reg.dist;
    ap_uint<64> out_reg;
    out_reg.range(31, 0) = *((ap_uint<32>*) (&cell_ID));
    out_reg.range(63, 32) = *((ap_uint<32>*) (&dist));
    output[0] = out_reg;
}